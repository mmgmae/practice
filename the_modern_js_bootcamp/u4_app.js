// IF STATEMENTS + Else if Statements
// Example 1
if (1 === 1) {
    console.log("It's True!");
}

// Example 2
let rating = 1;

if (rating === 3) {
    console.log('You are a superstar!');
}
else if (rating === 2) {
    console.log('Meets expectations');
}
else if (rating === 1) {
    console.log('Needs improvement');
}

// Example 3
let num = 40;

if (num % 2 !== 0) {
    console.log('Odd number');
}
else if (num % 2 === 0) {
    console.log('Even number');
}

